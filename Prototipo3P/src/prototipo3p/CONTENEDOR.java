 public void CentrarVentanas(JInternalFrame internalFrame){
        
        int Posicionx = (ventanap.getWidth( ) / 2)- internalFrame.getWidth() / 2;
        int Posiciony = (ventanap.getHeight() / 2)- internalFrame.getHeight() / 2;
        
       if (internalFrame.isShowing () ){
           internalFrame.setLocation (Posicionx,  Posiciony);
       }
    
    else {
    ventanap.add(internalFrame);
    internalFrame.setLocation(Posicionx,  Posiciony);
    internalFrame.show();
}
    }
    

    
    
